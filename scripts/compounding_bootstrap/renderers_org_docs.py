from __future__ import annotations

from .renderers_base_docs import evidence_boundary_block


def render_org_model() -> str:
    return f"""# 组织模型

## 设计原则

- 组织角色是稳定职责镜头，不是官僚部门
- 新增角色只有在相邻角色连续超载或职责冲突时才允许
- 角色长期低频或职责重叠时，优先合并，不保留空壳组织
- 组织设计职责并入 `总经办 / Foreman Office`，不单列 `HR`
- 规则服务于效率，不服务于自我扩张；若规则本身拖慢复利效率，可直接简化

## 决策层

### 总经办 / Foreman Office

- 使命：保持主线清晰、节奏稳定，确保系统始终像创业团队而不是大公司。
- 主要职责：
  - 决定当前主线、优先级和发布裁决
  - 负责组织设计、角色增删合并和规则收敛
  - 判断哪些规范该保留，哪些规范该删减
- 主要产物：
  - `AGENTS.md`
  - `memory/project/roadmap.md`
  - `memory/project/operating-blueprint.md`
  - 关键 ADR 与发布决策
- 何时介入：
  - 方向切换、优先级冲突、角色重叠、发布风险或规则失效时
- 不该做什么：
  - 不把任务管理做成审批流
  - 不把组织设计扩成多层部门树

## 交付层

### PMO

- 使命：把每次改动变成边界清晰、可追踪、可回写的 task。
- 主要职责：
  - 维护 task 清单与状态
  - 追踪更新痕迹和交付闭环
  - 组织复盘，确保经验被沉淀而不是丢失
- 主要产物：
  - `tasks/queue/*.md`
  - `tasks/archive/*`
  - `memory/project/operating-blueprint.md`
  - 复盘相关更新痕迹
- 何时介入：
  - 任何可合并改动开始前、交付中、交付完成后
- 不该做什么：
  - 不发明 lane、泳道、工时审批等重制度
  - 不替代产品、架构或工程做专业判断

### 产品

- 使命：把问题定义清楚，把价值判断和验收边界说清楚。
- 主要职责：
  - 定义需求、目标、范围与验收标准
  - 判断改动是否真正提升主线效率
  - 保证方案不是“做很多”，而是“做对重点”
- 主要产物：
  - task 的目标、范围、验收标准
  - `memory/project/operating-blueprint.md` 中的子目标和发布标准
  - 方案摘要与价值判断
- 何时介入：
  - 新需求进入、优先级竞争、验收标准不清时
- 不该做什么：
  - 不越过架构边界直接指定实现细节
  - 不把 roadmap 写成流水账

### 设计

- 使命：让信息架构、交互和视觉表达服务于快速理解。
- 主要职责：
  - 设计首页与关键页面的信息编排
  - 提供更优 UI/UX 方案与验收视觉标准
  - 避免“看起来专业但实际更难用”的设计
- 主要产物：
  - 页面信息架构
  - UI/UX 方案
  - 关键页面的视觉和交互要求
- 何时介入：
  - 首页、门户、阅读体验、结构变化或交互障碍出现时
- 不该做什么：
  - 不脱离内容真相源单独维护一套展示数据
  - 不为了炫技引入重型设计系统

## 研发层

### 架构

- 使命：让系统持续保持小模块、清边界、低耦合、易于 AI 理解。
- 主要职责：
  - 设计模块边界、接口契约、依赖方向
  - 收敛重复结构和巨型文件
  - 规划重构路线，控制复杂度扩张
- 主要产物：
  - `docs/ARCHITECTURE.md`
  - `code_index/*`
  - `module.md`
- 何时介入：
  - 结构重构、模块拆分、跨层依赖或大文件治理时
- 不该做什么：
  - 不把抽象层做成自我目的
  - 不把稳定规则和一次性经验混写

### 工程

- 使命：在最小上下文里把方案高质量落地，并持续删减旧逻辑。
- 主要职责：
  - 按 task、方案和验收标准实现改动
  - 删除被替代逻辑、收敛兼容层
  - 准备 release、执行切换或回滚修复
- 主要产物：
  - 代码改动
  - 发布准备与切换结果
  - 对应 task / docs / index 更新
- 何时介入：
  - 方案确认后进入实现、修复、重构和发布阶段时
- 不该做什么：
  - 不在没有 task 边界时直接开工
  - 不只加不删，留下长期兼容债

## 保障层

### 质量与度量

- 使命：用事实验证结果是否正确，用量化判断投入是否值得。
- 主要职责：
  - 验收结果与设计/方案是否匹配
  - 跟踪回归、风险、技术债和发布健康度
  - 量化评估工作量、成果和 ROI
- 主要产物：
  - 测试结果
  - 发布状态与风险清单
  - 技术债与量化结论
- 何时介入：
  - 验收、回归、发布前后、争议较大或 ROI 不明时
- 不该做什么：
  - 不把检查扩成形式主义流程
  - 不为了量化而量化，忽略主线价值

## 文化底线

- 规则服务于效率，不服务于扩张
- task 是边界，不是审批流
- roadmap 只记录主线变化
- 记忆只记录可复用经验和明确裁决
- 角色只是职责分工，不是组织层级炫耀
- 若规范本身限制复利效率，可直接简化规范

{evidence_boundary_block()}
"""
